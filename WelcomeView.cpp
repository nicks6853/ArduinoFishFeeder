void WelcomeView::draw() {
    Adafruit_SSD1306* display = fishFeeder->getDisplay();
    display->clearDisplay();

    // Print fish icon
    display->drawBitmap(0, 0, bitmap_fish, SCREEN_WIDTH, SCREEN_HEIGHT,
                        SSD1306_WHITE);

    // Print app name
    display->setCursor(64, 8);
    display->setTextColor(SSD1306_WHITE, SSD1306_BLACK);
    display->print("FISH");
    display->setCursor(64, 16);
    display->print("FEEDER");
    display->display();
}
